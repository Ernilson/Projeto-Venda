    private void Gravi() {
        con.conector();
        String caminho = jl.getIcon().toString();
        InputStream fis;
        File file = new File(caminho);
        //file.getName();
        String sql = "INSERT INTO img (id_img, foto_img) VALUES (?,?)";
        try {
            fis = new FileInputStream(file);         
            PreparedStatement ps = con.conn.prepareStatement(sql);
            ps.setString(1, TxtId.getText());
            ps.setBinaryStream(2, fis, (int) file.length());
           
            if(jl == null){
                 JOptionPane.showMessageDialog(null, "Preenchimento obrgatÃ³rio");
            } else {
           int adicionado = ps.executeUpdate();

                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Imagem inserida com sucesso");

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

